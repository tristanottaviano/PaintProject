    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            
            double x = Double.valueOf(jTextField1.getText());
            double y = Double.valueOf(jTextField2.getText());
            double r = Double.valueOf(jTextField3.getText());

            
             
            Ardoise ard = this.app.getArdoise();
            Point p1 = new Point(x, y,1);
            Point p2 = new Point(x, x+r,1);
            Cercle cir = new Cercle(p1, p2,ard.currBrushSize);
            ard.ajoute(cir);
            ard.repaint();
            this.setVisible(false);
                    
        }
        
        catch(Exception e){
            
            javax.swing.JFrame f = new javax.swing.JFrame();
            JOptionPane.showMessageDialog(f, "Please enter a Valid number");
            
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

